# Why is there a need to work with functions?

# Advantages
# 1) We can use it later by simply calling it with its name.
# 2) It removes the duplication of code from our program.
# 3) It makes the code a standardized one.
# 4) It improves the structure of our program.
# 5) It is easier to work with functions in a multi file project.
# 6) It is necessary for the object oriented programming.

# Disadvantages of functions?
# 1) There is a fixed syntax to create teh functions that's why tt is not easier to implement the functions.
# 2) In case of recursions, it can increase the time complexity of our program.
# 3) It distributes the functionality of your program.


